# Function to extract page number from chunk content
def get_page_number_from_chunk(chunk_content):
    # Look for '--- Page X ---' pattern
    match = re.search(r'--- Page (\d+) ---', chunk_content)
    if match:
        return int(match.group(1))
    return None # Or return a default like 0 or -1 if no page marker found

# Check if the text file exists
if not os.path.exists(output_text_file):
    print(f"Error: The text file '{output_text_file}' was not found.")
else:
    print(f"Loading text from: {output_text_file}")
    with open(output_text_file, 'r', encoding='utf-8') as f:
        full_text = f.read()

    # Initialize the RecursiveCharacterTextSplitter
    # These parameters can be tuned based on the nature of the text and desired chunk size
    text_splitter = RecursiveCharacterTextSplitter(
        chunk_size=500,  # Maximum number of characters in a chunk
        chunk_overlap=100,  # Overlap between chunks to maintain context
        length_function=len,  # Function to calculate chunk length
        add_start_index=True, # Add start index metadata initially, then custom add
    )

    # Split the text into chunks
    raw_texts = text_splitter.create_documents([full_text])

    # Now, add custom metadata to each chunk
    processed_texts = []
    for i, chunk in enumerate(raw_texts):
        # Create a new Document object or modify existing one
        new_metadata = chunk.metadata.copy()

        # Add chunk_id
        new_metadata["chunk_id"] = f"text_{i:04d}" # e.g., text_0000, text_0001

        # Add source
        new_metadata["source"] = os.path.basename(pdf_path) # Just the filename

        # Extract page number
        page_num = get_page_number_from_chunk(chunk.page_content)
        new_metadata["page"] = page_num if page_num is not None else "Unknown" # Handle cases where page marker might be missing

        # Add chunk_type
        new_metadata["chunk_type"] = "text"

        # Add placeholder for section
        # Automatic section detection is complex and requires specific document structure/parsing.
        # For now, we'll use a placeholder.
        new_metadata["section"] = "General" # Placeholder

        processed_texts.append(Document(page_content=chunk.page_content, metadata=new_metadata))

    texts = processed_texts # Update 'texts' to the processed list

    print(f"Created {len(texts)} chunks with custom metadata.")

    # Display the first few chunks with their new metadata
