@app.post("/query")
async def query_documents(request: QueryRequest):
    if collection is None:
        raise HTTPException(status_code=503, detail="ChromaDB collection not initialized.")

    print(f"Received query: '{request.query}' with n_results: {request.n_results}")
    try:
        # Step 1: Retrieve relevant documents from ChromaDB
        results = collection.query(
            query_texts=[request.query],
            n_results=request.n_results,
            include=['documents', 'metadatas', 'distances']
        )

        retrieved_docs_content = []
        formatted_results = []
        if results and results['documents'] and results['documents'][0]:
            for i in range(len(results['documents'][0])):
                doc_content = results['documents'][0][i]
                doc_metadata = results['metadatas'][0][i]
                doc_distance = results['distances'][0][i]

                retrieved_docs_content.append(doc_content)
                formatted_results.append({
                    "content": doc_content,
                    "metadata": doc_metadata,
                    "distance": doc_distance
                })

        # Step 2: Generate an answer using the LLM if available
        if openai_client:
            if retrieved_docs_content:
                context_str = "\n\n".join(retrieved_docs_content)
            else:
                context_str = "No relevant documents found in the knowledge base."

            messages = [
                {"role": "system", "content": f"You are an AI assistant for a document. Use the following context to answer the question. If you don't know the answer, just say that you don't know, don't try to make up an answer.\n\nContext: {context_str}"},
                {"role": "user", "content": f"Question: {request.query}"}
            ]

            response = openai_client.chat.completions.create(
                extra_headers={
                    "HTTP-Referer": "https://colab.research.google.com/", # Required for OpenRouter rankings
                    "X-Title": "Google Colab Notebook", # Optional
                },
                model="qwen/qwen3.6-plus:free", # Updated to 'qwen/qwen3.6-plus:free'
                messages=messages,
                max_tokens=512,
                temperature=0.1
            )
            llm_response = response.choices[0].message.content
            return {"query": request.query, "answer": llm_response, "sources": formatted_results}
        else:
            # If LLM is not initialized, return only retrieval results
            return {"query": request.query, "answer": "LLM not initialized. Showing raw retrieval results.", "sources": formatted_results}

    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Error during query processing: {str(e)}")

# Function to gracefully stop previous uvicorn instances on the port
def stop_fastapi_server(port):
    print(f"Checking for existing processes on port {port}...")
    for proc in psutil.process_iter(['pid', 'name', 'connections']):
        try:
            for conn in proc.connections(kind='inet'):
                if conn.laddr.port == port:
                    print(f"Found process {proc.name()} (PID: {proc.pid}) using port {port}. Terminating...")
                    proc.terminate()
                    proc.wait(timeout=3) # Wait for termination
                    if proc.is_running():
                        proc.kill() # Force kill if still running
                        print(f"Process {proc.pid} force killed.")
                    else:
                        print(f"Process {proc.pid} terminated successfully.")
        except (psutil.NoSuchProcess, psutil.AccessDenied, psutil.ZombieProcess):
            pass
    print("Cleanup complete.")

