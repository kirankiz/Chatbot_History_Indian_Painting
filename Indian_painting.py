image_index_file = 'image_index.json'
refined_illustrations_directory = 'refined_illustrations'
image_summaries_output_file = 'image_summaries.json'

# 1. Load image_index.json
if not os.path.exists(image_index_file):
    print(f"Error: {image_index_file} not found. Please ensure the image indexing cell was run successfully.")
else:
    with open(image_index_file, 'r', encoding='utf-8') as f:
        image_index_data = json.load(f)
    print(f"Loaded {len(image_index_data)} image entries from {image_index_file}.")

    # 2. Initialize the Image-to-Text Model with 4-bit quantization
    print("\nInitializing image-to-text model (llava-hf/llava-1.5-7b-hf with 4-bit quantization). This may take some time for download and setup...")
    try:
        # Define 4-bit quantization configuration
        nf4_config = BitsAndBytesConfig(
            load_in_4bit=True,
            bnb_4bit_quant_type="nf4",
            bnb_4bit_use_double_quant=True,
            bnb_4bit_compute_dtype=torch.float16 # Use float16 for computation
        )

        # Load the model with quantization config
        model = LlavaForConditionalGeneration.from_pretrained(
            "llava-hf/llava-1.5-7b-hf",
            quantization_config=nf4_config,
            device_map="auto" # Automatically maps the model to available devices
        )
        # Load the processor (tokenizer and image processor)
        processor = AutoProcessor.from_pretrained("llava-hf/llava-1.5-7b-hf")

        # Create the pipeline using the loaded model and processor
        image_to_text_pipe = pipeline(
            "image-text-to-text", # Changed task name to "image-text-to-text" as this is now the standard for multimodal models
            model=model,
            processor=processor # Pass the entire processor object
        )
        print("Image-to-text model loaded successfully with 4-bit quantization.")
    except Exception as e:
        print(f"Error loading image-to-text model: {e}")
        print("Please ensure you have run `!pip install -U transformers bitsandbytes accelerate` and have sufficient resources.")
        image_to_text_pipe = None # Indicate failure to load model

    if image_to_text_pipe:
        image_summaries = []
        print("\nGenerating summaries for extracted images...")

        for i, img_entry in enumerate(image_index_data):
            image_id = img_entry['image_id']
